import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "likes")
public class LikeEntity {

    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Long id;

    @Column(name = "user_id")
    private Long userId;

    @Column(name = "report_id")
    private Long reportId;

    // コンストラクタ、ゲッター、セッターなどのメソッド

}
